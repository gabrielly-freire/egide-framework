package br.imd.ufrn.egide.enums;

// Categorias de classificação de uma manifestação, atribuídas pelo módulo de IA durante a triagem.
// DENUNCIATION: denúncia formal de irregularidade.
// COMPLAINT: reclamação sobre serviço ou atendimento.
// COMPLIMENT: elogio a servidor ou serviço.
// SUGGESTION: proposta de melhoria.
// REQUEST: solicitação de informação ou providência.
public enum ReportCategory {
    DENUNCIATION,
    COMPLAINT,
    COMPLIMENT,
    SUGGESTION,
    REQUEST
}
